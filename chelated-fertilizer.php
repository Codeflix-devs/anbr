                </p>
                <p><b>Manganese (Mn)</b> is required for photosynthesis and respiration. It improves green colour and increases sugar and protein content. Manganese enhances plant tolerance to high light intensity. <br>
                Typical deficiency symptoms: chlorotic mosaic patterns on leaves.
                </p>
                <p><b>Copper (Cu)</b> is essential for chlorophyll formation, cell wall building and many enzymatic mechanisms. It supports seed formation and reproduction, 
