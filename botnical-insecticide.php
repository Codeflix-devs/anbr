                <p>Among the botanical insecticides currently marketed, neem oil is one of the least toxic to humans and shows very low toxicity to beneficial organisms, so it is, therefore, very promising for the control of many pests.   </p>
                <p>The oil is considered a contact insecticide, presenting systemic and translaminar activity. It has a broad spectrum of action, inhibiting feeding, affecting hormone function in juvenile stages, reducing ecdysone, deregulating growth, altering development and reproduction, suppressing fertility, sterilizing, repelling oviposition, and disrupting molting processes . Little is known about the mode of action of azadirachtin as a feeding inhibitor, although it is possible that it stimulates cells involved in feeding inhibition, causing weakness and pest death.         
