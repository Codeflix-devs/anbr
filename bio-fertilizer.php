                <p>For a sustainable agriculture system, it is essential to use renewable inputs (fertilizer, pesticides, water etc.) which benefit the plant and cause no or minimal damage to the environment. One possible way is to reduce the use of chemical fertilizers and pesticides. Chemical fertilizers. are being used in increasing amounts in order to increase the output in high yielding varieties of crop plants.  However, chemical fertilizers cause pollution of water bodies as well as groundwater, besides getting stored in crop plants.</p>
            
                <p> Environmentalists worldwide are pressing the market and society for a switch over to organic farming and biofertilizers. Organic farming aims to be a more environmentally sustainable form of agricultural production, combining best environmental practices, and emphasizing biodiversity protection and the preservation of natural resources. Organic farming is one such strategy that not only ensures food safety, but also adds to the biodiversity of soil. 
                Organic farming is the raising of unpolluted crops through the use of manures, biofertilizers and biopesticides that provide optimum nutrients to crop plants, keeping pests and pathogens under control.</p>
                
                <h5>WHAT ARE BIOFERTILIZERS? </h5>
                <p>Generally, the term "fertilizer" is used for "fertilizing material or carrier", meaning any substance which contains one or more of the essential elements (nitrogen, phosphorus, potassium, sulphur, calcium, magnesium, iron, manganese, molybdenum, copper, boron, zinc, chlorine, sodium, cobalt, vanadium and silicon). Thus, fertilizers are used to improve the fertility of the land.</p>
                <p>Biofertilizers are also living or biologically active products or microbial inoculants of bacteria, algae and fungi (separately or in combination) which are able to enrich the soil with nitrogen, phosphorus, organic matter etc. Biofertilizers act as a compound that enriches the nutrient quality of the soil by using microorganisms that establish symbiotic relationships with the plants. </p>
                <p>Biofertilizers are low-cost renewable sources of plant nutrients which supplement chemical fertilizers. Biofertilizers generate plant nutrients like nitrogen, phosphorus and potassium through their activities in the soil or rhizosphere and make them available to the plants on the soil. </p>
                <p><b>Microbial biofertilizers: Bioresources and eco-friendly technologies for agricultural and environmental sustainability</b></p>
                <p><b>The use of biofertilizers is gaining importance because of the proper maintenance of soil health, the minimization of environmental pollution and the cut-down in the use of chemicals.</b></p>
                <p> Biofertilizers are one of the important components of integrated nutrient management, as they are a cost-effective and renewable source of plant nutrients to supplement and/or replace the chemical fertilizers for sustainable agriculture. These are preparations containing living cells or latent cells of efficient strains of microorganisms that help the uptake of nutrients in crop plants by their interactions in the rhizosphere when applied through seed or soil. They accelerate certain microbial processes in the soil which augment the extent of availability of nutrients in a form easily assimilated by plants.</p>

                <h5>Vesicular Arbuscular Mycorrhizal (VAM) Fungi: A flagship product of ANBR Elixir Private Limited</h5>
                <p>In the current agricultural system, synthetic/chemical fertilizers are widely used. Excessive usage of these chemical fertilizers over a period of time has many adverse effects on the soil health and quality, and also pollute water bodies. So using VAM fungi as a biocontrol agent in modern sustainable agriculture, in terms of various parameters like reduction of damage caused by various pathogens, cost effectiveness, energy saving and also as an environment friendly, is a promising perspective in modern agriculture. In the present agricultural system increasing demand for low-input agriculture and creates greater interest in soil microorganisms which are able to accelerate plant nutrition, health and improve soil quality. The importance of VAM in increasing food production is far and wide; therefore these can be used in modern sustainable agriculture particularly as biocontrol agent. </p>

                <h5>ECTO MYCORRHIZAE ( ECM ) Fungi</h5>
                <p>ANBR Elixir Private Limited also offers Ectomycorrhiza (ECM)  Fungi, its associations involve the most diverse category of mycorrhizae. ECM fungi make associations with plants by forming a sheath (mantle) around fine root tips with hyphae that grow inward between root cells of the cortex and make Hartig net, and emanate outward through the soil, increasing the surface area to absorb nutrients and water. Thus, the mycorrhizal fungi gain vital elements for photosynthesis and other essential substances from the plant and in return help the plant take up water and minerals. ECM associations, beside fertilization, also increase the supply of water to the pines, and elevate host resistance against disease and parasites. ECM fungi improve the growth of host plants at the seedling stage.. This association has been successfully applied to reforestation programs in tropical forests by inoculating mycorrhizae on to nursery seedlings.</p>
