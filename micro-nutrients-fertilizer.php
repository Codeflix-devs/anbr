                <h5>The role of micronutrients in a plant as below:</h5>
                <ul>
                    <li>Zinc is crucial for plant hormone balance and auxin activity and it is vital for growth, a division of cells and production of husks of grains.</li>
                    <li>Iron assists in biological processes and cell growth in plants. Iron is a component of enzymes, essential for chlorophyll synthesis, photosynthesis.</li>
                    <li>Boron enhances flowering blooms and develops uniform ripening process and it is essential in sugar transport, Cell division, and amino acid production.</li>
                    <li>Manganese performs an important role in photosynthesis, chloroplast production, a cofactor in many plant reactions and activates enzymes.</li>
                    <li>Copper stimulates enzymes required for photosynthesis.</li>
                    <li>Molybdenum is crucial for the growth of leaves and prevention of diseases in plants and it is involved in nitrogen metabolism, essential in nitrogen fixation by legumes.</li>
                </ul> 
                <h5>Micronutrient Deficiency Symptoms:</h5>
                <p>Except for Molybdenum, Other micronutrients are considered weakly mobile or immobile in plants. This means that deficiency symptoms appear severely on newest plant tissues, whereas for Molybdenum deficiency symptoms appear first on oldest plant tissues.</p>
                <ul>
                    <li>Zinc deficiency results in stunted growth, lessened internode length, young leaves are smaller than normal.</li>
                    <li>Iron deficiency leads to chlorosis or yellowing between the veins of new leaves.</li>
                    <li>Boron deficiency results into light general chlorosis, a death of growing point, deformed leaves with areas of discoloration.</li>
                    <li>Manganese (Mn) deficiency cause chlorotic mosaic patterns on leaves.</li>
                    <li>Copper deficiency results in light overall chlorosis, leaf tips die back and tips are twisted, loss of turgor in young leaves.</li>
                    <li>Molybdenum deficiency is similar to those of ordinary nitrogen deficiency â€“ general chlorosis (yellowing) of young plants, chlorosis of oldest leaves.</li>
                </ul>
