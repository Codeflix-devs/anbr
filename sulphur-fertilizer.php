                <h5>Role of Sulphur in Plant Growth and Development</h5>
                <p>Sulphur is one of the 17 essential plant nutrients. It is essential for the growth and development of all crops, without exception. Like any essential nutrient, sulphur also has some key functions in plants:</p>
                <p>
                    •   Formation of chlorophyll that permits photosynthesis through which plants produce starch, sugars, oils, fats, vitamins and other compounds.<br>
                    •   Protein production. Sulphur is a constituent of three S-containing amino acids (cysteine, cystine and methionine), which are the building blocks of protein. About 90% of plant S is present in these amino acids.<br>
                    •   Synthesis of oils. This is why adequate sulphur is so crucial for oilseeds.<br>
                    •   Activation of enzymes, which aid in biochemical reactions in the plant.<br>
                    •   Increases crop yields and improves produce quality, both of which determine the market price a farmer would get for his produce.<br>
                    •   With reference to crop quality, S improves protein and oil percentage in seeds, cereal quality for milling and baking, marketability of dry coconut kernel (copra), quality of tobacco, the nutritive value of forages, etc.<br>
                    •   It is associated with special metabolisms in plant and the structural characteristics of protoplasm.<br>
                </p>
                <h3>Sulphur Fertilizer Types</h3>
                <p>With more than 20 different commercially available sulphur-containing fertilizers, it may be a challenge for farmers to choose the best option.  </p>
                <h5>Elemental Sulphur</h5>
                <p>The use of elemental S to reduce soil pH and to reclaim sodic soils is well known. However, its function as a fertilizer source of S has greatly increased its use in agriculture with the limited production and availability of other sulphur-containing fertilizer materials.<br>
                Elemental S is a yellow, inert, water-insoluble crystalline solid. Commercially it is stored in the open, where it remains unaltered by moisture and temperature changes. When S is finely ground and mixed with soil, however, it is oxidized to sulphate by soil microorganisms. The effectiveness of S in supplying S to plants compared with sulphate depends on several factors, including particle size, rate, method, and of application; S oxidizing characteristics of the soil; and environmental conditions. S oxidation rate increases as particle size is reduced. The finer the S particle size, the greater the surface area and the faster the sulphate formation. Thus, increases in the S surface area results in increased sulphate availability to crops.<br>
                Uniform distribution of S particles throughout the soil will 1) provide greater exposure of S particles to oxidizing microorganisms, 2) minimize any potential concerns caused by excessive acidity, and 3) provide more favorable moisture relationships.<br>                
                </p>
                <h5>Sulphur Bentonite</h5>
                <p><b>A variety of S-bentonite fertilizers have been produced to improve the effectiveness of granular elemental S products by incorporating, typically 10% by weight of swelling clay such as bentonite. Particles of S-bentonite are sized for blending with solid N,P, and K fertilizers. When it is applied to soil, this bentonite component imbibes soil moisture, causing fertilizer granules to disintegrate into finely divided S, which is more rapidly converted to sulphate. This material has gained wide acceptance as a source of plant nutrient S for high analysis, bulk blend formulations because it provides elemental S in an acceptable physical form that can be converted easily into the sulphate form in soil.</b><br>
                Because of variable oxidation rate of these S sources during the first growing season after application, it should be incorporated into soil prior to planting. When it is applied just before seeding and on severely S deficient soils, some sulphate should also be provided. Repeated use of elemental S containing fertilizers tends to gradually enlarge the population of S-oxidizing microorganisms, resulting in a corresponding increase in the rate of sulphate formation.
                </p>
