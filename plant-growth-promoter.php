                <h5>Types of Plant Hormones</h5>
                <h5>Abscisic Acid</h5>
                    <p>The original name of abscisic acid was dormin, because the plant hormones are heavily involved in the dormancy process. They regulate the process of seed development. This helps transform the embryo into a fully-fledged seedling. Also, these plant hormones play a crucial role in the plant’s response to temperature and water loss.</p>
                    <p>As the temperature increases, more water evaporates out of the stoma, little holes in the leaves. As the temperature reaches a point that starts dramatic water loss, abscisic acid is produced and released into the leaves. This causes the stoma to close, and the water is retained within the leaves. Without these plant hormones, plants could not regulate their water content. This is an important and necessary function of vascular plants.</p>
                <h5>Auxins</h5>
                    <p>Auxins are a class of plant hormones responsible for various aspects of plant growth. Typically, they affect cell enlargement and elongation. They also allow the plant to react to sunlight and gravity. In many plants, auxins are responsible for establishing the growth direction of the plant.</p>
                    <p>The concentration of these plant hormones also directs the development in different parts of the plant, depending on the species. In fruit trees, auxins are involved in initiating the flowing and finishing the fruiting process. In potatoes and carrots, auxins are involved in the regulation and storage of starches in the roots.</p>
                <h5>Cytokinins</h5>
                    <p>The cytokinins are a group of plant hormones that interact directly with the auxins. In doing so, they direct cell differentiation and various aspects of cell metabolism. The cytokinins interact with the plant’s DNA, making it express or hide various proteins. This directs cell differentiation within the plant, allowing the plant to develop different tissues for different purposes.</p>
                    <p>Unlike the auxins, the cytokinins are most concentrated in the roots, and become less concentrated towards the shoots. This counterbalance to auxin allows the plant to develop and maintain an axis, and grow in both directions. Cytokinins applied without auxins produce roots, auxins alone produce buds, and combined they tend to form undifferentiated growth. </p>
                <h5>Ethylene</h5>
                    <p>Unlike the other plant hormones, ethylene is a single chemical. It is in the form of a gas at regular temperatures and allows plants a fast way to communicate between cells and other plants. Ethylene is typically found in plants any time damage is incurred. When a stem is bent, bruised, or broken, ethylene is released. As a gas, ethylene quickly diffuses through the fluids of the plant, and can travel through the air. Plants use this hormone to communicate damage to other plants, stimulating them to ripen their fruit or to develop defenses against herbivores.</p>
                    <p>Ethylene induces fruits, nuts, and vegetables to finish growing and to detach from the stem. This ensures easy collection.</p>
                <h5>Gibberellins</h5>
                    <p>Gibberellins, like the auxins, are regulatory plant hormones. They, to a greater extent than the auxins, control cell division and overall plant growth. Dwarf plants often have a genetic defect in which gibberellins cannot be produced or utilized. A dwarf plant exposed to extra gibberellins will grow to a normal size. Gibberellins are also responsible for activating a number of enzymes.</p>
                    <p>Some plants use gibberellins as sexual hormones, helping drive the development of male and female flowers. Along with the auxins, gibberellin plant hormones affect senescence of plant parts. Gibberellins also have an important role in bringing seeds out of dormancy. Gibberellins in the seed activate enzymes such as amylases, which will break down starches to glucose and provide the embryo with energy. The plant hormones also activate other enzymes, which provide the embryo with amino acids and lipids to grow.</p>
                <h5>Other Plant Hormones</h5>
                    <p>There are four more basic plant hormone groups that have been discovered in recent years. The brassinolides are steroid hormones, similar to estrogen and testosterone. These plant hormones have some function in cell division, while it is not entirely clear how they act. Salicylic acid, another recently discovered hormone, acts like ethylene and allows plants to communicate between individuals. These plant hormones react to pathogens and attack, like an immune system hormone. Another class, the jasmonates, represents a similar plant hormone. Lastly, systemin is a plant defense hormone class involved in activating the defense genes of various plants after a portion of their system has been injured.</p>
