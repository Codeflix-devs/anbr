                <p>Knowing the NPK values of a fertilizer can help you select one that is appropriate for the type of plant you are growing. For example, if you are growing leafy vegetables, you may want to apply a fertilizer that has a higher nitrogen number to encourage leaf growth. If you are growing flowers, you may want to apply a fertilizer that has a higher phosphorus number to encourage more blooms.</p>
                <p>Before you apply fertilizer to your farm/garden etc, , you should have your soil tested. This will also help you determine what balance of fertilizer numbers will be appropriate for your garden’s soil needs and deficiencies.</p>

                <h5>Homogeneous NPKs - ARIHANT</h5>
                <p>The range of NPK fertilizers has a balance of nitrogen, phosphorus and potassium that suits specific nutritional needs of your crop throughout its growing season. In addition, the range includes formulations with secondary nutrients (magnesium and sulfur) and micronutrients (boron, iron, manganese, molybdenum, and zinc). These homogeneous NPK fertilizers will help you maximize crop yield and quality. Each compound, whether in prill or granule form, is designed for high value crops such as vegetables, fruit trees, vines and turf. NPK fertilizers benefit the grower because they provide even distribution of nutrients, whether spread by hand or machine. There is no risk of nutrient segregation during shipping, handling or spreading.</p>

                <h5>Efficient nutrient sources</h5>
                <ul>
                    <li><b>Balanced nitrogen source</b> - Nitrogen is present at 40-45% as nitrate-N and 55-60% as ammonium-N, depending on the production process and the specific formula.</li>
                    <li><b>Fast nitrogen uptake</b> - High nitrate-N content ensures a quick response to nitrogen, while the ammonium-N is important to keep a sustained delivery of nitrogen.</li>
                    <li><b>Available phosphate</b> - Phosphorus is fully plant available, as water soluble ortho- and polyphosphates and ammonium citrate soluble di-calcium phosphate. The combination of different forms give greater and a longer lasting availability of phosphorus to crops.</li>
                    <li><b>Alternative potassium sources</b> - Potassium is included as MOP (Muriate of Potash - potassium chloride) or SOP (Sulfate of Potash -potassium sulfate). Products based on SOP are specially for crops with low chloride tolerance whilst MOP based products are suitable for all other crops.</li>
                </ul>  
                <p>Efficient fertilizers and fertilization methods have become the main answer to the ever-growing demand for agricultural products. Modern agriculture must supply crops with optimal rates of nutrients throughout the growth cycle in the most efficient manner possible, and without degrading soil and water resources. Fertigation and foliar nutrition enable highly efficient use of nutrients. </p>

                <h5>Knowing NPK: Your Soil, Made Better</h5>
                <p>Your field needs to eat, just like you do. And just like how you track carbohydrates, protein, and fat, your plants need a specific breakdown of nutrients, too. That’s where NPK – nitrogen, phosphorus, and potassium – comes in. While plant nutrients are found in nature, each field’s plot of soil can vary. So, field products will often come with these nutrients already in perfect proportions for what you’re growing.</p>
                <h5>What Does NPK Stand For?</h5>
                <p>If our food is comprised of three main nutrients, you can bet that your plants’ food is, too. And just as each of those nutrients has specific functions for fueling our bodies, the soil’s nutrients also have their own responsibilities in supporting a field’s health.</p>
                <h5>N: Nitrogen</h5>
                <p>Within the ratio, N stands for nitrogen. This is the naturally-occurring element that makes up a large part of the air we breathe. But the nitrogen in the air isn’t enough to feed our plants, so we rely on organic matter and field products to provide enough of this nutrient from the roots up. Nitrogen is responsible for the growth of leaves on a plant. In fact, you may be able to diagnose a nitrogen deficiency with yellowing leaves and skinny stalks. Nitrogen also helps plants create chlorophyll, a green pigment in plants that allows for the absorption of sunlight into photosynthesis. Green plants and vegetables typically have higher nitrogen requirements. </p>
                <h5>P: Phosphorus</h5>
                <p>Another vital nutrient in a plant’s growth is phosphorus. It helps to convert other nutrients into usable building blocks by developing a plant’s root system. It typically affects a plant’s ability to flower or fruit and is known as the “bloom booster.” That said, you may notice a phosphorus deficiency when plants:</p>

                <ul>
                    <li>Are smaller than they should be.</li>
                    <li>Produce little (or no) flowers.</li>
                    <li>Have weak root systems.</li>
                    <li>Appear to have a purple tint.</li>
                </ul>
                <p>Plants that fruit or produce blooms are more likely to need higher phosphorus levels, and the best time to apply phosphorus to the field is early spring before you’d expect blooms to occur. Phosphorus field products can come in both organic and synthetic versions, though. When it comes to synthetic phosphorus, be wary of it leaching into your water supply and creating a heavy salt buildup.  </p>
                <h5>K: Potassium</h5>
                <p>While not exactly intuitive, “K” is the symbol for potassium, which is the third macronutrient that your plants depend on. Potassium helps the plant perform overall. It strengthens the cell to increase disease resistance, promote growth and durability, and create a larger fruit set. While phosphorus is responsible for increased blooms, potassium can help increase the size of that bloom – especially helpful in growing fruit. Potassium is a primary reason forest fires can be beneficial, as the formation of wood ash can unlock potassium and renew the area’s life. Providing this nutrient to your plants helps to make them hardier against external elements, from the weather to pests. While it’s hard to identify a potassium-deficiency, yellowing edges, leaves curling downward, and susceptibility to disease can all be indicators that you should add potassium to your field.</p>
                <h5>Types of Fertilizers</h5>
                <p>Each field, each plant, each seed needs nutrients to grow.  Fertilizers play a large role in giving seeds nutrients and growing strong healthy plants.  With all of the types of fertilizers, which one is best?  That answer depends on which plant is needing fertilized and at what stage of life the plant is at.</p>
