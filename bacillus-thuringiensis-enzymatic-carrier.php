                    <p><b>How does it work?</b><br>
                    Bacillus thuringiensis must be ingested by a susceptible host to be effective. When ingested, Bacillus thuringiensis produces proteins that react with the cells of the stomach lining. These (proteins) poison and paralyse the insect's digestive system causing the insect to stop feeding within hours. Bt-infested insects will live for several days but will cause no further damage to the plant. They will die eventually from starvation. </p>
                    <p><b>How to use Bacillus thuringiensis (Bt)?</b><br><br>
                    •	Spray thoroughly, covering all the plant surfaces.<br> 
                    •	Apply when larvae are less than 5 mm long or when the eggs begin to hatch. Bt works best on young larvae. <br>
                    •	 In the hot tropics, it is more effective to spray Bt in the late afternoon as there are longer and cooler hours ahead. This enables Bt to remain longer on the leaves' surfaces. Bt survives better in cooler temperature. Whereas, spraying in the morning provides a shorter and hotter environment. <br>
                    •	Do not mix the Bt concentrate with alkaline water (pH 8 or higher). Alkalinity reduces its effectiveness. To make the water acidic, add a few tablespoons of white vinegar in a gallon of water before adding Bt.<br>
                    </p> 
