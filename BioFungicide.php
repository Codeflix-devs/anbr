                    <h5>How Bio fungicides Work?</h5>
                    <p><b>Rhizosphere Competence</b> - The most successful of the strains of biocontrol microorganisms exhibit rhizosphere competence, the ability to colonize and grow in association with plant roots. They can colonize entire root surfaces for several months.  Here they effectively compete with plant pathogens for nutrients, infection sites, and space. Competition for glucose in the soil is involved in disease suppression.  Bio fungicide organisms also metabolize seed and root exudates that normally stimulate pathogen germination or zoospore attraction.</p>
                    <p><b>Parasitism</b> - Parasitism, the ability of species to attack and consume plant pathogens, has been well studied. Mycoparasitism of biocontrol microorganisms includes directed growth, contact and binding, coiling of hyphae around the host fungus, penetration, and degradation. Production of cell wall degrading enzymes is almost always part of the process.</p> 
                    <p><b>Antibiosis</b> - Antibiosis occurs when one microorganism produces molecules that directly affect other organisms negatively by toxicity or growth inhibition. These compounds are called antibiotics and are commonly produced by a wide range of soil-dwelling microorganisms in the course of their growth. </p> 
                    <p><b>Inducing Metabolic Changes</b> - An important mechanism of biocontrol microorganisms is the ability to induce metabolic changes in plants that increase their resistance to a wide range of plant pathogenic fungi and bacteria. Systemic Acquired Resistance (SAR) improves the plant response to pathogen attack by priming the production of plant defense compounds. This capacity to induce resistance to a wide range of diseases in a variety of plants appears to be widespread. Enhanced resistance is systemic because disease resistance occurs at sites distant from the location of the biocontrol microorganisms.</p>
                    <p><b>Plant Growth Promotion</b> - A final way in which these organisms act is through plant growth promotion. Beneficial root-colonizing microorganisms promote plant growth and productivity. Many resistance-inducing fungi and bacteria promote both root and shoot growth in the absence of plant pathogens. When applied to growing media or as a seed treatment, some bio fungicides can increase root development and improve drought resistance in some plants.  Improvements in plant growth result from effects on soil microflora and direct effects on the plant. Biofungicides can also improve nutrient uptake (copper, phosphorous, iron, and manganese).</p>
