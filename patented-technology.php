                <p> The methods used for productivity enhancement during GR have not only proven to be problematic but have also resulted in deterioration of soil quality and several other issues related to ecosystems and health issues. The damage was mainly caused by the indiscriminate use of chemical fertilizers and insecticides. Various types of pesticides, are now known to be causing huge problems in the agro-ecosystems. </p>
                <p> In such a situation, where chemicals have caused, or are causing irreversible impacts on agroecosystems, the use of biopesticides has emerged as a sustainable alternative leading to safe organic farming. At the global level, environmentally benign nature and target-specificity of biopesticides are gaining wide popularity. </p>
