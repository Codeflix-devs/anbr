                <p> <b>Microbial and biochemical pesticides</b> are increasingly used as soil amendments or seed treatments that will target the necessary area of the plant. When microorganisms are added to the soil/plant complex they release families of biochemical molecules to a targeted environment, such as the surface of the leaf or stem or in the root rhizosphere. The pesticidal properties of the microbial biochemical excretions then aid the plant in its affected areas.</p>
                <P>The most widely used microbial pesticide are types of bacteria Bacillus thuringiensis or Bt. Each strain of bacterium produces different proteins that are toxic to certain insects, specifically targeting insect larvae.</P>

